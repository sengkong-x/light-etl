CREATE TABLE ETL_MAPPINGS (
    ID            NUMBER GENERATED BY DEFAULT AS IDENTITY,
    SRC_OBJ       VARCHAR2(128) NOT NULL,
    DST_OBJ       VARCHAR2(128) NOT NULL,
    IS_ACTIVE     NUMBER(1) DEFAULT 1,
    LAST_RUN_DATE DATE,
    CONSTRAINT PK_ETL_MAPPINGS PRIMARY KEY (ID),
    CONSTRAINT UQ_ETL_MAPPINGS_SRC_DST UNIQUE (SRC_OBJ, DST_OBJ),
    CONSTRAINT CK_ETL_MAPPINGS_ID NOT NULL (ID),
    CONSTRAINT CK_ETL_MAPPINGS_SRC NOT NULL (SRC_OBJ),
    CONSTRAINT CK_ETL_MAPPINGS_DST NOT NULL (DST_OBJ)
);
